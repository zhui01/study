package com.heartsuit.showcase.domain.author;

/**
 * The type Ordinary author.普通作者
 */
public class OrdinaryAuthor extends Author {
    private String author; // 作者名
    private String name; // 作者名

    /**
     * Gets author.
     *
     * @return the author
     */
    public String getAuthor() {
        return author;
    }

    /**
     * Sets author.
     *
     * @param author the author
     */
    public void setAuthor(String author) {
        this.author = author;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }
}
